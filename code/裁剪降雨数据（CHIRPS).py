def 格式化字节大小(字节数):
    """
    将字节数转换为更易读的大小字符串。

    参数说明：
    - 字节数：文件大小，单位为字节。
    """
    单位列表 = ["B", "KB", "MB", "GB", "TB"]
    大小 = float(max(字节数, 0))
    单位索引 = 0

    while 大小 >= 1024 and 单位索引 < len(单位列表) - 1:
        大小 /= 1024
        单位索引 += 1

    return f"{大小:.2f} {单位列表[单位索引]}"


def 打印步骤进度(步骤编号, 步骤总数, 步骤名称):
    """
    打印单个文件处理过程中的关键步骤进度。

    参数说明：
    - 步骤编号：当前步骤编号，从 1 开始。
    - 步骤总数：单个文件总步骤数。
    - 步骤名称：当前步骤的中文说明。
    """
    完成比例 = 步骤编号 / 步骤总数
    已完成宽度 = int(完成比例 * 进度条宽度)
    进度条 = "█" * 已完成宽度 + "-" * (进度条宽度 - 已完成宽度)
    print(f"[步骤进度] {步骤编号}/{步骤总数} |{进度条}| {完成比例:>6.1%} | {步骤名称}")


